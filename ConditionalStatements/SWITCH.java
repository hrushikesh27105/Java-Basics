package ConditionalStatements;

public class SWITCH {
    public static void main(String[] args) {
        int num=2;
        switch(num){
            case 1:System.out.println("order burger.");
            break;

            case 2:System.out.println("order Pizza.");
            break;
            case 3:System.out.println("order Sanddwich.");
            break;
            default:System.out.println("buy softdrinks.");

        }
    }
    
}
